    PROCEDURE CalculateStudentAverages AS
        grades SYS_REFCURSOR;
    BEGIN
        OPEN grades FOR
        SELECT 
            s.firstname || ' ' || s.lastname AS full_name,
            AVG(sc.markofstd) AS average_grade
        FROM 
            student s
        JOIN 
            std_course sc ON s.studentid = sc.stdid
        GROUP BY 
            s.firstname, s.lastname;

        DBMS_SQL.RETURN_RESULT(grades);
    END CalculateStudentAverages;

    PROCEDURE TopStudentsByGrades(num_students IN NUMBER) AS
        students SYS_REFCURSOR;
    BEGIN
        OPEN students FOR
        SELECT 
            s.firstname || ' ' || s.lastname AS full_name,
            AVG(sc.markofstd) AS average_grade
        FROM 
            student s
        JOIN 
            std_course sc ON s.studentid = sc.stdid
        GROUP BY 
            s.firstname, s.lastname
        ORDER BY 
            average_grade DESC
        FETCH FIRST num_students ROWS ONLY;

        DBMS_SQL.RETURN_RESULT(students);
    END TopStudentsByGrades;

    PROCEDURE CountCoursesByCategory AS
        c_course_count SYS_REFCURSOR;
    BEGIN
        OPEN c_course_count FOR
        SELECT 
            c.categoryname,
            COUNT(co.courseid) AS num_courses
        FROM 
            category c
        LEFT JOIN 
            course co ON c.categoryid = co.categoryid
        GROUP BY 
            c.categoryname;

        DBMS_SQL.RETURN_RESULT(c_course_count);
    END CountCoursesByCategory;

    PROCEDURE StudentsBornInYear(p_year IN NUMBER) AS
        c_students_year SYS_REFCURSOR;
    BEGIN
        OPEN c_students_year FOR
        SELECT 
            s.firstname || ' ' || s.lastname AS full_name,
            s.dateofbirth
        FROM 
            student s
        WHERE 
            EXTRACT(YEAR FROM s.dateofbirth) = p_year;

        DBMS_SQL.RETURN_RESULT(c_students_year);
    END StudentsBornInYear;

    PROCEDURE TopThreeCoursesByEnrollment AS
        c_top_courses SYS_REFCURSOR;
    BEGIN
        OPEN c_top_courses FOR
        SELECT 
            c.coursename,
            COUNT(sc.stdid) AS enrollment_count
        FROM 
            course c
        JOIN 
            std_course sc ON c.courseid = sc.courseid
        GROUP BY 
            c.coursename
        ORDER BY 
            enrollment_count DESC
        FETCH FIRST 3 ROWS ONLY;

        DBMS_SQL.RETURN_RESULT(c_top_courses);
    END TopThreeCoursesByEnrollment;

